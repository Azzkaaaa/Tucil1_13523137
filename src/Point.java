public class Point {

    /* ========== ATTRIBUTES ========== */
    private float X;
    private float Y;

    /* ============ GETTER ============ */
    public float getX() {
        return this.X;
    }

    public float getY() {
        return this.Y;
    }

    /* ========== CONSTRUCTOR ========= */
    public Point(float x, float y) {
        this.X = x;
        this.Y = y;
    }
}